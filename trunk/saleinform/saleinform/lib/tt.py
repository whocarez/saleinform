class Node:
    def __init__(self, n, s):
        self.id = n
        self.title = s
        self.children = []

class CategoriesTree:
    treeMap = {}
    Root = Node(0, "Root")
    treeMap[Root.id] = Root
    
    def __init__(self, categories):
        for element in categories:
            nodeId, parentId, title = element
            if not nodeId in self.treeMap:
                self.treeMap[nodeId] = Node(nodeId, title)
            else:
                self.treeMap[nodeId].id = nodeId
                self.treeMap[nodeId].title = title

            if not parentId in self.treeMap:
                self.treeMap[parentId] = Node(0, '')
            self.treeMap[parentId].children.append(self.treeMap[nodeId])     

